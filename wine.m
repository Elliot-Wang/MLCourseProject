% 一个关于酒类品质的数据集
% 数据集信息：
% 这些数据是对意大利同一地区种植的葡萄酒进行化学分析的结果，这些葡萄酒来自三个不同的品种。该分析确定了三种葡萄酒中每种葡萄酒中含有的13种成分的数量。 
% 我认为初始数据集有大约30个变量，但出于某种原因我只有13维版本。
% 
% 属性是（由Riccardo Leardi，riclea '@' anchem.unige.it捐赠） 
% 1）酒精 
% 2）苹果酸 
% 3）灰 
% 4）灰分的碱度
% 5）镁 
% 6）总酚
% 7） 黄酮类 化合物 
% 8）非黄烷类酚类 
% 9）原花色素 
% 10）颜色强度 
% 11）色调 
% 12）稀释葡萄酒的OD280 / OD315 
% 13）脯氨酸 
% 
